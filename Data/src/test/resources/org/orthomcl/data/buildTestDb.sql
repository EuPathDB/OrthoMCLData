CREATE SCHEMA APIDB;
CREATE SCHEMA DOTS;

CREATE TABLE APIDB.ORTHOMCLTAXON (
  TAXON_ID INTEGER NOT NULL,
  NAME VARCHAR(25),
  THREE_LETTER_ABBREV VARCHAR(3),
  IS_SPECIES INTEGER,
  PRIMARY KEY (TAXON_ID)
);

CREATE TABLE APIDB.ORTHOLOGGROUP (
  ORTHOLOG_GROUP_ID INTEGER NOT NULL,
  NAME VARCHAR(25),
  NUMBER_OF_MEMBERS INTEGER,
  LAYOUT_CONTENT VARCHAR(100),
  PRIMARY KEY (ORTHOLOG_GROUP_ID)
);

CREATE TABLE APIDB.ORTHOLOGGROUPAASEQUENCE (
  AA_SEQUENCE_ID INTEGER,
  ORTHOLOG_GROUP_ID INTEGER
);

CREATE TABLE DOTS.EXTERNALAASEQUENCE (
  AA_SEQUENCE_ID INTEGER,
  SOURCE_ID VARCHAR(100),
  TAXON_ID VARCHAR(100),
  LENGTH INTEGER,
  DESCRIPTION VARCHAR(100)
);
